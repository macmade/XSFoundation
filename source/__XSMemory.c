/*!
 * @file        __XSMemory.c
 * @brief       Private implementation for the memory functions and the XSAutoreleasePoll class
 * @author      Jean-David Gadina <macmade@eosgarden.com>
 */

#include "XS.h"
#include "__XSMemory.h"
#include "__XSMemoryDebug.h"

/*!
 * @var         __xsmemory_ar_pools
 * @brief       An array with the auto-release pool objects
 */
__XSAutoreleasePool * __xsmemory_ar_pools[ XS_MEMORY_MAX_AR_POOLS ];

/*!
 * @var         __xsmemory_ar_pools_num
 * @brief       The number of active auto-release pool objects
 */
XSUInteger __xsmemory_ar_pools_num;

/*!
 * @var         __XSAutoreleasePoolClass
 * @brief       Runtime class definition
 */
static const XSClassInfos __XSAutoreleasePoolClass =
{
    "XSAutoreleasePool",            /* Class name */
    sizeof( __XSAutoreleasePool ),  /* Object size */
    NULL,                           /* Constructor */
    __XSAutoreleasePool_Destruct,   /* Destructor */
    XSAutoreleasePool_Init,         /* Default initializer */
    __XSAutoreleasePool_Copy,       /* Object copy */
    NULL,                           /* Object description */
    NULL                            /* Object comparison */
};

/*!
 * @var         __XSAutoreleasePoolClassID
 * @brief       Type ID for the runtime class
 */
XSClassID __XSAutoreleasePoolClassID;

void __XSAutoreleasePool_Initialize( void )
{
    __XSAutoreleasePoolClassID = XSRuntime_RegisterClass( &__XSAutoreleasePoolClass );
    
    XSRuntime_BindMethodToClassID( __XSAutoreleasePoolClassID, ( void ( * )( void ) )XSAutoreleasePool_Init, "Init", "void" );
}

void __XSAutoreleasePool_Destruct( XSObject object )
{
    XSDebugLog( XSDebugLogLevelDebug, "Destroying auto-release pool: %p", ( void * )object );
    
    __XSMemory_AutoreleasePoolDrain( ( __XSAutoreleasePool * )object );
    XSRelease( ( ( __XSAutoreleasePool * )object )->objects );
    __xsmemory_ar_pools_num--;
}

void __XSAutoreleasePool_Copy( XSObject source, XSObject destination )
{
    __XSAutoreleasePool * a1;
    __XSAutoreleasePool * a2;
    XSUInteger            i;
    
    a1 = ( __XSAutoreleasePool * )source;
    a2 = ( __XSAutoreleasePool * )destination;
    
    for( i = 0; i < a2->numObjects; i++ )
    {
        XSRetain( a2->objects[ i ] );
    }
}

__XSAutoreleasePool * __XSMemory_GetCurrentAutoreleasePool( void )
{
    if( __xsmemory_ar_pools_num == 0 )
    {
        return NULL;
    }
    
    return __xsmemory_ar_pools[ __xsmemory_ar_pools_num - 1 ];
}

void __XSMemory_AutoreleasePoolDrain( __XSAutoreleasePool * ap )
{
    size_t i;
    
    XSDebugLog( XSDebugLogLevelDebug, "Draining auto-release pool: %p", ( void * )ap );
    
    for( i = 0; i < ap->numObjects; i++ )
    {
        XSRelease( ap->objects[ i ] );
    }
    
    ap->numObjects = 0;
}

__XSMemoryObject * __XSMemory_GetMemoryObject( void * ptr )
{
    __XSMemoryObject * o;
    char             * c;
    
    c  = ( char * )ptr;
    c -= sizeof( __XSMemoryObject );
    o  = ( __XSMemoryObject * )c;
    
    return o;
}
