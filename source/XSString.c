BOOL XSString_IsEqualToString( XSStringRef str1, XSStringRef str2 )
{
    XSString * _str1;
    XSString * _str2;
    
    _str1 = ( XSString * )str1;
    _str2 = ( XSString * )str2;
    
    if( strcmp( _str1->str, _str2->str ) == 0 )
    {
        return YES;
    }
    
    return NO;
}

