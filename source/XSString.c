XSStringRef XSString_SubstringFromIndex( XSStringRef str, size_t i )
{
    XSString * _str;
    
    _str = ( XSString * )str;
    
    if( i >= _str->length )
    {
        return NULL;
    }
    
    return XSString_CreateWithCString( _str->str + i );
}

