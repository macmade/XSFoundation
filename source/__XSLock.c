/*!
 * @file        __XSLock.c
 * @brief       Private implementation for the XSLock class
 * @author      Jean-David Gadina <macmade@eosgarden.com>
 */

#include "XS.h"
#include "__XSLock.h"

/*!
 * @var         __XSLockClass
 * @brief       Runtime class definition
 */
static const XSClassInfos __XSLockClass =
{
    "XSLock",           /* Class name */
    sizeof( __XSLock ), /* Object size */
    __XSLock_Construct, /* Constructor */
    __XSLock_Destruct,  /* Destructor */
    XSLock_Init,        /* Default initializer */
    NULL,               /* Object copy */
    __XSLock_ToString,  /* Object description */
    NULL                /* Object comparison */
};

/*!
 * @var         __XSLockClassID
 * @brief       Type ID for the runtime class
 */
XSClassID __XSLockClassID;

void __XSLock_Initialize( void )
{
    __XSLockClassID = XSRuntime_RegisterClass( &__XSLockClass );
    
    XSRuntime_BindMethodToClassID( __XSLockClassID, ( void ( * )( void ) )XSLock_Init, "Init", "XSObject" );
    XSRuntime_BindMethodToClassID( __XSLockClassID, ( void ( * )( void ) )XSLock_Lock, "Lock", "BOOL" );
    XSRuntime_BindMethodToClassID( __XSLockClassID, ( void ( * )( void ) )XSLock_TryLock, "TryLock", "BOOL" );
    XSRuntime_BindMethodToClassID( __XSLockClassID, ( void ( * )( void ) )XSLock_WaitForLock, "WaitForLock", "void" );
    XSRuntime_BindMethodToClassID( __XSLockClassID, ( void ( * )( void ) )XSLock_Unlock, "Unlock", "void" );
    XSRuntime_BindMethodToClassID( __XSLockClassID, ( void ( * )( void ) )XSLock_IsLocked, "IsLocked", "BOOL" );
}

void __XSLock_Construct( XSObject object )
{
    __XSLock * lock;
    
    lock = ( __XSLock * )object;
    
    pthread_mutex_init( &( lock->mutex ), NULL );
}

void __XSLock_Destruct( XSObject object )
{
    __XSLock * lock;
    
    lock = ( __XSLock * )object;
    
    pthread_mutex_destroy( &( lock->mutex ) );
}

XSString __XSLock_ToString( XSObject object )
{
    if( ( ( __XSLock * )object )->locked == YES )
    {
        return XSSTR( ( char * )"Locked" );
    }
    
    return XSSTR( ( char * )"Unlocked" );
}
