/*!
 * @file        __XSNotification.c
 * @brief       Private implementation for the XSNotification class
 * @author      Jean-David Gadina <macmade@eosgarden.com>
 */

#include "XS.h"
#include "__XSNotification.h"

/*!
 * @var         __XSNotificationClass
 * @brief       Runtime class definition
 */
static const XSClassInfos __XSNotificationClass =
{
    "XSNotification",               /* Class name */
    sizeof( __XSNotification ),     /* Object size */
    NULL,                           /* Constructor */
    __XSNotification_Destruct,      /* Destructor */
    XSNotification_Init,            /* Default initializer */
    __XSNotification_Copy,          /* Object copy */
    __XSNotification_ToString,      /* Object description */
    NULL                            /* Object comparison */
};

/*!
 * @var         __XSNotificationClassID
 * @brief       Type ID for the runtime class
 */
XSClassID __XSNotificationClassID;

void __XSNotification_Initialize( void )
{
    __XSNotificationClassID = XSRuntime_NewClass( &__XSNotificationClass );
}

void __XSNotification_Destruct( XSObject object )
{
    __XSNotification * notification;
    
    notification = ( __XSNotification * )object;
    
    if( notification->object != NULL )
    {
        XSRelease( notification->object );
    }
    
    if( notification->name != NULL )
    {
        XSRelease( notification->name );
    }
}

XSString __XSNotification_ToString( XSObject object )
{
    XSString description;
    
    description = XSString_Init( XSString_Alloc() );
    
    XSString_AppendFormat
    (
        description,
        ( char * )"%s (%p)",
        XSString_CString( ( ( __XSNotification * )object )->name ),
        ( ( __XSNotification * )object )->object
    );
    
    return XSAutorelease( description );
}

void __XSNotification_Copy( XSObject source, XSObject destination )
{
    __XSNotification * n1;
    __XSNotification * n2;
    
    n1 = ( __XSNotification * )source;
    n2 = ( __XSNotification * )destination;
    
    n2->name = XSCopy( n1->name );
    
    XSRetain( n2->object );
}
