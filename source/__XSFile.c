void __XSFile_WriteAlign( __XSFile * file )
{
    if( file->bit_count > 0 )
    {
        fputc( file->bit_buffer, file->fp );
    }
    
    file->bit_count  = 0;
    file->bit_buffer = 0;
    file->bit_offset = 0;
}

void __XSFile_UpdateStat( __XSFile * file )
{
    stat( file->filename, &( file->stat_buf ) );
}
