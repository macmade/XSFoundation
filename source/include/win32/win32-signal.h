#endif

#ifndef SIGILL
#define SIGILL      0x05
#endif

#ifndef SIGINT
#define SIGINT      0x06
#endif

#ifndef SIGKILL
#define SIGKILL     0x07
#endif

#ifndef SIGPIPE
#define SIGPIPE     0x08
#endif

#ifndef SIGQUIT
#define SIGQUIT     0x09
#endif

#ifndef SIGSEGV
#define SIGSEGV     0x0A
#endif

#ifndef SIGTERM
#define SIGTERM     0x0B
#endif

#ifndef SIGUSR1
#define SIGUSR1     0x0C
#endif

#ifndef SIGUSR2
#define SIGUSR2     0x0D
#endif

#ifndef SIGCHLD
#define SIGCHLD     0x0E
#endif

#ifndef SIGCONT
#define SIGCONT     0x0F
#endif

