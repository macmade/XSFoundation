/*!
 * @file        XSDebugLog.c
 * @copyright   eosgarden 2011 - Jean-David Gadina <macmade@eosgarden.com>
 * @abstract    Implementation of the debug log functions
 */

#include "XS.h"

static XSDebugLogLevel __xsdebuglog_level = 0;

#if defined( XS_DEBUG_LOG_ENABLED ) && XS_DEBUG_LOG_ENABLED == 0
void XSDebugLogEnable( XSDebugLogLevel level );
#endif
void XSDebugLogEnable( XSDebugLogLevel level )
{
    __xsdebuglog_level  |= level;
}

#if defined( XS_DEBUG_LOG_ENABLED ) && XS_DEBUG_LOG_ENABLED == 0
void XSDebugLogDisable( XSDebugLogLevel level );
#endif
void XSDebugLogDisable( XSDebugLogLevel level )
{
    __xsdebuglog_level  &= ~( level );
}

#if defined( XS_DEBUG_LOG_ENABLED ) && XS_DEBUG_LOG_ENABLED == 0
void XSDebugLog( XSDebugLogLevel level, const char * format, ... );
#endif
void XSDebugLog( XSDebugLogLevel level, const char * format, ... )
{
    va_list args;
    
    if( level & __xsdebuglog_level )
    {
        va_start( args, format );
        
        printf( "    --- DEBUG LOG - " );
        
        switch( level )
        {
            case XSDebugLogLevelEmergency: printf( "EMERGENCY: " ); break;
            case XSDebugLogLevelAlert:     printf( "ALERT: " );     break;
            case XSDebugLogLevelCritical:  printf( "CRITICAL: " );  break;
            case XSDebugLogLevelError:     printf( "ERROR: " );     break;
            case XSDebugLogLevelWarning:   printf( "WARNING: " );   break;
            case XSDebugLogLevelNotice:    printf( "NOTICE: " );    break;
            case XSDebugLogLevelInfo:      printf( "INFO: " );      break;
            case XSDebugLogLevelDebug:     printf( "DEBUG: " );     break;
            case XSDebugLogLevelAll:       printf( "UNKNOWN: " );   break;
            default:                       printf( "UNKNOWN: " );   break;
        }
        
        vprintf( format, args );
        printf( "\n" );
        
        va_end( args );
    }
}
